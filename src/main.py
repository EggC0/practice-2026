@bot.callback_query_handler(func=lambda call: True)
def callback_query(call):
    if call.data.startswith("sign_"):
        sign = call.data.split("_")[1]
        bot.answer_callback_query(call.id, f"Выбран знак: {sign.capitalize()}")
        
        if not hasattr(bot, "user_data"):
            bot.user_data = {}
        bot.user_data[call.message.chat.id] = {"sign": sign}

        bot.edit_message_text(
            f"Вы выбрали знак {sign.capitalize()}. Теперь выберите дату (23–30 мая):",
            chat_id=call.message.chat.id,
            message_id=call.message.message_id,
            reply_markup=date_keyboard()
        )
    elif call.data.startswith("date_"):
        date_str = call.data.split("_")[1] 
        chat_id = call.message.chat.id
        user_data = bot.user_data.get(chat_id, {})
        sign = user_data.get("sign")
        if not sign:
            bot.answer_callback_query(call.id, "Ошибка: знак не выбран. Начните заново /horoscope")
            return
        horoscope_text = horoscope_data.get(sign, {}).get(date_str)
        if not horoscope_text:
            horoscope_text = f"Извините, предсказание для {sign.capitalize()} на {date_str} временно отсутствует."

        user_id = call.from_user.id
        username = call.from_user.username
        first_name = call.from_user.first_name
        log_user(user_id, username, first_name, f"выбрал {sign}, дату {date_str}")

        bot.send_message(chat_id, horoscope_text)
        bot.delete_message(chat_id, call.message.message_id)
        if chat_id in bot.user_data:
            del bot.user_data[chat_id]

