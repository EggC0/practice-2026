LOG_FILE = "bot_log.txt"

def log_user(user_id, username, first_name, action):
    timestamp = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    log_entry = f"[{timestamp}] User ID: {user_id} | Username: {username} | Name: {first_name} | Action: {action}\n"
    try:
        with open(LOG_FILE, "a", encoding="utf-8") as f:
            f.write(log_entry)
    except Exception as e:
        print(f"Ошибка записи лога: {e}")

def sign_keyboard():
    keyboard = telebot.types.InlineKeyboardMarkup(row_width=3)
    signs = [
        "Aries", "Taurus", "Gemini", "Cancer", "Leo", "Virgo",
        "Libra", "Scorpio", "Sagittarius", "Capricorn", "Aquarius", "Pisces"
    ]
    buttons = [telebot.types.InlineKeyboardButton(sign, callback_data=f"sign_{sign.lower()}") for sign in signs]
    keyboard.add(*buttons)
    return keyboard

def date_keyboard():
    keyboard = telebot.types.InlineKeyboardMarkup(row_width=4)

    dates = [
        ("23 мая", "2026-05-23"), ("24 мая", "2026-05-24"),
        ("25 мая", "2026-05-25"), ("26 мая", "2026-05-26"),
        ("27 мая", "2026-05-27"), ("28 мая", "2026-05-28"),
        ("29 мая", "2026-05-29"), ("30 мая", "2026-05-30")
    ]
    buttons = [telebot.types.InlineKeyboardButton(label, callback_data=f"date_{date_val}") for label, date_val in dates]
    keyboard.add(*buttons)
    return keyboard

@bot.message_handler(commands=['start', 'hello'])
def send_welcome(message):
    user = message.from_user
    log_user(user.id, user.username, user.first_name, "/start")
    bot.reply_to(message, "Привет! Я бот-гороскоп. Нажми /horoscope, чтобы получить предсказание на любой день с 23 по 30 мая.")

@bot.message_handler(commands=['horoscope'])
def sign_handler(message):
    user = message.from_user
    log_user(user.id, user.username, user.first_name, "/horoscope")
    bot.send_message(message.chat.id, "Выберите ваш знак зодиака:", reply_markup=sign_keyboard())

